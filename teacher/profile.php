<?php
error_reporting(E_ALL);
ini_set('display_errors', 1);
session_start();
require_once '../config/database.php';

// Check if user is logged in and is a teacher
if (!isset($_SESSION['user_id']) || $_SESSION['role'] !== 'teacher') {
    header('Location: ../login.php');
    exit();
}

$teacher_id = $_SESSION['user_id'];
$error_message = '';
$success_message = '';

// Get teacher information
$teacher_sql = "SELECT username, email FROM users WHERE id = ?";
$teacher_stmt = $conn->prepare($teacher_sql);
$teacher_stmt->bind_param("i", $teacher_id);
$teacher_stmt->execute();
$teacher_result = $teacher_stmt->get_result();
$teacher = $teacher_result->fetch_assoc();

// Handle form submission
if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    $username = trim($_POST['username']);
    $current_password = $_POST['current_password'];
    $new_password = $_POST['new_password'];
    $confirm_password = $_POST['confirm_password'];

    try {
        // Start transaction
        $conn->begin_transaction();

        // Update username if changed
        if ($username !== $teacher['username']) {
            $update_sql = "UPDATE users SET username = ? WHERE id = ?";
            $update_stmt = $conn->prepare($update_sql);
            $update_stmt->bind_param("si", $username, $teacher_id);
            $update_stmt->execute();
        }

        // Update password if provided
        if (!empty($current_password) && !empty($new_password) && !empty($confirm_password)) {
            // Verify current password
            $verify_sql = "SELECT password FROM users WHERE id = ?";
            $verify_stmt = $conn->prepare($verify_sql);
            $verify_stmt->bind_param("i", $teacher_id);
            $verify_stmt->execute();
            $stored_password = $verify_stmt->get_result()->fetch_assoc()['password'];

            if (!password_verify($current_password, $stored_password)) {
                throw new Exception('Current password is incorrect.');
            }

            if ($new_password !== $confirm_password) {
                throw new Exception('New passwords do not match.');
            }

            $hashed_password = password_hash($new_password, PASSWORD_DEFAULT);
            $update_sql = "UPDATE users SET password = ? WHERE id = ?";
            $update_stmt = $conn->prepare($update_sql);
            $update_stmt->bind_param("si", $hashed_password, $teacher_id);
            $update_stmt->execute();
        }

        $conn->commit();
        $success_message = 'Profile updated successfully!';
        
        // Refresh teacher data
        $teacher_stmt->execute();
        $teacher = $teacher_stmt->get_result()->fetch_assoc();
    } catch (Exception $e) {
        $conn->rollback();
        $error_message = $e->getMessage();
    }
}
